    boolean isValidatedNhanVien() {
        if (txtMaUser.getText().isEmpty()) {
            MsgBox.alert(this, "Không để trống mã nhân viên!");
            return false;
        }
        if (txtTen.getText().isEmpty()) {
            MsgBox.alert(this, "Không để trống họ và tên nhân viên!");
            return false;
        }
        try {
            if (txtSDT.getText().isEmpty()) {
                MsgBox.alert(this, "Số điện thoại chưa được nhập!");
                return false;
            }
            double salary = Double.parseDouble(txtSDT.getText());
            Pattern pattern = Pattern.compile("0\\d{7,10}");
            Matcher matcher = pattern.matcher(txtSDT.getText());
            if (!matcher.find()) {
                MsgBox.alert(this, "Định dạng số điện thoại không hợp lệ!");
                return false;
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Số điện thoại không hợp lệ (Phải là số)!");
            return false;
        }
        SimpleDateFormat sdf = new SimpleDateFormat();
        sdf.applyPattern("dd-MM-yyyy");
        try {
            if (txtNgaySinh.getText().isEmpty()) {
                MsgBox.alert(this, "Không để trống ngày sinh!");
                return false;
            }
            Date date = sdf.parse(txtNgaySinh.getText());
        } catch (Exception e) {
            MsgBox.alert(this, "Ngay khong hop le");
            return false;
        }
        try {
            if (txtEmail.getText().isEmpty()) {
                MsgBox.alert(this, "Email chưa được nhập!");
                txtEmail.requestFocus();
                return false;
            }
            Pattern pattern = Pattern.compile("\\w+@\\w+\\.\\w+");
            Matcher matcher = pattern.matcher(txtEmail.getText());
            if (!matcher.find()) {
                MsgBox.alert(this, "Định dạng email không hợp lệ!");
                return false;
            }
        } catch (HeadlessException e) {
        }
        try {
            if (txtLuong.getText().isEmpty()) {
                MsgBox.alert(this, "Số lương chưa được nhập!");
                return false;
            }
            double number = Double.parseDouble(txtLuong.getText());
            if (number < 0) {
                MsgBox.alert(this, "Số lương không được là số âm!");
                return false;
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lương không hợp lệ!!!");
            return false;
        }

        return true;
    }

