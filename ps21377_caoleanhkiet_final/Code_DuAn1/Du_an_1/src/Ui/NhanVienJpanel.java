    private void fillTableNhanVien() {
        DefaultTableModel model = (DefaultTableModel) tblThongTinNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<User> listnv = daonv.selectAll();
            for (User nv : listnv) {
                Object[] row = {
                    nv.getIDUser(),
                    nv.getTen(),
                    nv.getSDT(),
                    nv.isGioiTinh() ? "Nam" : "Nữ",
                    nv.getNgaySinh(),
                    nv.getEmail(),
                    nv.getDiaChi(),
                    nv.getLuong(),
                    nv.isVaiTro() ? "Chủ" : "Nhân Viên",
                    nv.isTrangThai() ? "Còn làm" : "Nghỉ làm"
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

